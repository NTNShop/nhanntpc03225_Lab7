export const uppercaseString = (strings) => {
    console.log('Chuỗi in hoa: ');
    return strings.toUpperCase();
};
export const lowercaseString = (strings) => {
    // eslint-disable-next-line indent
    console.log('Chuỗi in thường: ');
    return strings.toLowerCase();
};